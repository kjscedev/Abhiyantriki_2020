$(".technical-btn").click(function () {
  $("button").css({
    color: "white",
    "background-color": "transparent",
    "border-color": "white",
  });
  $(this).css({
    color: "black",
    "background-color": "white",
    "border-color": "black",
  });
  $(".cards").css({ display: "none" });
  $(".technical-section").css({ display: "grid" });
});

